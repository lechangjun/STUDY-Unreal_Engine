using System;

namespace Language_Study
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.Write(30); // int 형
            Console.WriteLine(30.1); // double 형
            Console.WriteLine(30.2f); // float 형
            Console.Write(true); // bool 형
            Console.Write('p'); // char 형
        }
    }
}

